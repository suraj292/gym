Looking for ways to mix up our non-alcoholic seltzers in creative ways? Here are some tips from the Shapes team. With a few simple tips and recipes, you can make your summertime tipples just like those big cans of alcohol. Hangover-free and easy, too.

With a splash of seltzer, we love mixing up a light lunch with roasted corn, an orange, and a little lime or lemon juice and coconut oil. It will feel good and taste delicious – a perfect summertime lunch.

This summer, our team has crafted several refreshing recipes using fresh fruits and vegetables.

Drinking seltzer might not have the same hangover-fighting power as drinking wine. However, mixing seltzer with a cocktail is a delicious, healthy alternative. We like serving our refreshing blends with a fresh fruit such as mango, pineapple, strawberries, and peaches.

With some fresh fruits, herbs, and ice pack, and hot water, you’ll have delicious, healthy, refreshing drinks at your fingertips in just a few minutes. We’re making these seltzer recipes from the Shapes team to inspire you to make your own summer cocktails in style – seltzer of course!

We also know that a dose of seltzer might be a good hangover cure for some. And you can find that recipe too – we’ve made this healthy mix with greens to support detoxification (and the taste) of hangovers. Enjoy it with dinner with grilled chicken.

Recipe: Citrus Glazed Acorn Squash, Summer Greens, Cilantro, Pineapple Chutney

This tropical blend of citrusy tastes amazing with our citrus-enhanced mixed berries.

Recipe: Citrus Veggie Shake

Easy to make and delicious, this recipe uses vegetables that are easy to cook, pureed to achieve a smooth, almost frozen consistency, and served chilled. It feels indulgent and delicious at the same time, like drinking a blend of wine, fruit, and vegetables. It’s like having a summertime cocktai for dinner.

The best way to make seltzer is by using seltzer juice. Not that we’re hating on sparkling water, but it just doesn’t have the same flavor as seltzer.

Pour 1 liter of seltzer over 1 liter of ice, mix well, and serve chilled. Your favorite cocktails will taste fabulous.

Simple – yet delicious. While you might not feel the hangover with seltzer mixed with your drink, mixing your seltzer cocktail with a cocktail will certainly cure your hangover.

Cocktail Recipe: Light and Bright Cocktail

